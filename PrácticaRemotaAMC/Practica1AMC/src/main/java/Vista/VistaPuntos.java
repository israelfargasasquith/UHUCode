    @Override
    public void paint(Graphics g) {

        super.paint(g);
        //g=PanelPuntos.getGraphics();
        g = this.PanelPuntos.getGraphics();
        int minX = 500, minY = 500, maxX = 0, maxY = 0;
        int panelWidth = PanelPuntos.getWidth();
        int panelHeight = PanelPuntos.getHeight();
        ArrayList<Punto> puntosRescalados = new ArrayList<>();

        if (puntos.size() != 0) {

            for (Punto p : puntos) {

                minX = (int) Math.min(minX, p.getX());
                maxX = (int) Math.max(maxX, p.getX());
                minY = (int) Math.min(minY, p.getY());
                maxY = (int) Math.max(maxY, p.getY());

                //g.fillOval((int)p.getX()*TamMult,(int) p.getY()*TamMult, TamPuntos,TamPuntos);
            }

            // Calculamos factores de escala
            double scaleX = panelWidth / (maxX - minX);
            System.out.println(panelHeight);
            System.out.println(panelWidth);
            double scaleY = panelHeight / (maxY - minY);

            //Dibujamos los puntos escalados
            for (Punto p : puntos) {

                double x = (p.getX() - minX) * scaleX;
                double y = (p.getY() - minY) * scaleY;
                g.fillOval((int)x,(int)y, TamPuntos,TamPuntos);
            }

        }

    }

